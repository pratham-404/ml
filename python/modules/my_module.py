
def triangle_area(b,h):
    """Area of Triangle

    Args:
        b (float): length of base
        h (float): length of height

    Returns:
        _type_: float
    """
    return (b*h)/2

def square_area(a):
    """Area of Square

    Args:
        a (float): length of a side

    Returns:
        _type_: float
    """
    return float(a*a)